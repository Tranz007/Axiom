function jsDoc(value) {
  return value.replace(/\*\//g, "* /");
}

function stringify(value) {
  return JSON.stringify(value, null, 2);
}

function policyMatrix(graph) {
  return graph.capabilities.map((capability) => ({
    capability: capability.name,
    purpose: capability.sections.purpose || [],
    reads: capability.reads,
    disclosureModes: capability.disclosureModes,
    forbiddenDisclosure: capability.forbiddenDisclosure,
    decisions: capability.policy.decisions,
    approval: capability.hasApproval || capability.policy.decisions.some((decision) => decision.kind === "require_approval"),
    broker: capability.sections.broker || [],
    audit: capability.auditEvents,
  }));
}

function capabilitiesTs(graph) {
  const data = graph.capabilities.map((capability) => ({
    key: capability.name,
    purpose: capability.sections.purpose || [],
    reads: capability.reads,
    disclosureModes: capability.disclosureModes,
    forbiddenDisclosure: capability.forbiddenDisclosure,
    decisions: capability.policy.decisions,
  }));

  return `/* Generated by Axiom. Do not edit directly. */
export const axiomCapabilities = ${stringify(data)} as const;

export type AxiomCapabilityKey = (typeof axiomCapabilities)[number]["key"];
`;
}

function dataClassesTs(graph) {
  const data = graph.dataClasses.map((dataClass) => ({
    key: dataClass.name,
    sensitivity: dataClass.sensitivity,
    allowedDisclosure: dataClass.allowedDisclosure,
    defaultApproval: dataClass.defaultApproval,
  }));

  return `/* Generated by Axiom. Do not edit directly. */
export const axiomDataClasses = ${stringify(data)} as const;

export type AxiomDataClassKey = (typeof axiomDataClasses)[number]["key"];
`;
}

function auditEventsTs(graph) {
  const data = graph.capabilities.map((capability) => ({
    capability: capability.name,
    audit: capability.auditEvents,
  }));

  return `/* Generated by Axiom. Do not edit directly. */
export const axiomAuditObligations = ${stringify(data)} as const;
`;
}

function runtimeGuardsTs(graph) {
  return `/* Generated by Axiom. Do not edit directly. */
import { axiomCapabilities } from "./capabilities";

export type AxiomDecision = "allow" | "deny" | "require_approval";

export function getCapabilityContract(capabilityKey: string) {
  return axiomCapabilities.find((capability) => capability.key === capabilityKey);
}

export function assertKnownCapability(capabilityKey: string) {
  const contract = getCapabilityContract(capabilityKey);
  if (!contract) {
    throw new Error(\`Unknown Axiom capability: \${capabilityKey}\`);
  }
  return contract;
}

export function assertDisclosureMode(capabilityKey: string, disclosureMode: string) {
  const contract = assertKnownCapability(capabilityKey);
  if (!contract.disclosureModes.includes(disclosureMode as never)) {
    throw new Error(\`Disclosure mode \${disclosureMode} is not declared for \${capabilityKey}\`);
  }
}

export const axiomApp = ${JSON.stringify(graph.app?.name || "unknown")};
`;
}

function reportMarkdown(graph, diagnostics, target) {
  const errors = diagnostics.filter((item) => item.severity === "error");
  const warnings = diagnostics.filter((item) => item.severity === "warning");

  return `# Axiom Verification Report

App: ${graph.app?.name || "unknown"}
Target: ${target}

## Summary

- Capabilities checked: ${graph.capabilities.length}
- Actors checked: ${graph.actors.length}
- Data classes checked: ${graph.dataClasses.length}
- Errors: ${errors.length}
- Warnings: ${warnings.length}

## Diagnostics

${diagnostics.length ? diagnostics.map((item) => `- **${item.severity.toUpperCase()}** ${item.message}${item.ref ? ` (${item.ref})` : ""}`).join("\n") : "- No diagnostics."}

## Generated Contracts

${policyMatrix(graph).map((item) => `### ${item.capability}

- Reads: ${item.reads.join(", ") || "none declared"}
- Disclosure: ${item.disclosureModes.join(", ") || "none declared"}
- Decisions: ${item.decisions.map((decision) => decision.kind).join(", ") || "none declared"}
- Approval path: ${item.approval ? "yes" : "no"}
`).join("\n")}
`;
}

export function generateArtifacts(graph, diagnostics, { target }) {
  if (target !== "typescript") {
    throw new Error(`Unsupported target ${target}. The MVP currently supports only typescript.`);
  }

  return [
    { path: "policy-matrix.json", contents: `${stringify(policyMatrix(graph))}\n` },
    { path: "capabilities.ts", contents: capabilitiesTs(graph) },
    { path: "data-classes.ts", contents: dataClassesTs(graph) },
    { path: "audit-events.ts", contents: auditEventsTs(graph) },
    { path: "runtime-guards.ts", contents: runtimeGuardsTs(graph) },
    { path: "axiom-report.md", contents: reportMarkdown(graph, diagnostics, target) },
    {
      path: "README.md",
      contents: `# Generated Axiom Artifacts

These files were generated from an Axiom source graph.

- \`policy-matrix.json\`: capability, data, disclosure, policy, broker, and audit matrix
- \`capabilities.ts\`: generated capability contracts
- \`data-classes.ts\`: generated sensitive data class contracts
- \`audit-events.ts\`: generated audit obligations
- \`runtime-guards.ts\`: minimal TypeScript runtime assertions
- \`axiom-report.md\`: validation and generation report

Regenerate with:

\`\`\`bash
axiom generate <file.ax> --target typescript --out generated
\`\`\`
`,
    },
  ];
}
